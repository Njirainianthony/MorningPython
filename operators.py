
#ARITHMETIC OPERATORS
num1=10
num2=3

print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1%num2) #Modulus - Returns remainder

#COMPARISON OPERATORS - COMPARE VALUES
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) # Equal To
print(num1 != num2) #Not Equal To

#ASSIGNMENT OPERATORS- Asign values to variables
x=100
print(x)

x+=2
print(x)

y=56
y-=3
print(y)

#LOGIC OPERATORS - and, or, not
a=23
print(a<40 and a<2)
print(a<40 or a<2)
print(not(a<40 or a<2))

#OPERATOR PRECEDENCE - order in which operations get executed
result=(10+6/2-4)
print(result)
print(int(result))






